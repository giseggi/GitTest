package Day1;

public class Day01 {
	
	public static void main(String[] args) {
		
		
		int A = 5;
		int B = 3;
		
		
		System.out.println("A < B = " + (A < B));
		System.out.println("A <= B = " + (A <= B));
		System.out.println("A > B = " + (A > B));
		System.out.println("A >= B = " + (A >= B));
		System.out.println("A == B = " + (A == B));
		System.out.println("A != B = " + (A != B));
		
	}
}
